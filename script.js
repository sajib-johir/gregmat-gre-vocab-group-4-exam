function scoreQuestion(q,idx){
  const a=answers[idx]; let marks=0, correct=false, details=[];
  if(q.type==='mcq'){marks=a?.value===q.answer?1:0;correct=marks===1;details.push({label:'Answer',student:a?.value||'Not answered',correct:q.answer,ok:correct});}
  else if(q.type==='typed'){const val=norm(a?.value||'');const accepted=q.answers.map(norm);marks=accepted.includes(val)?1:0;correct=marks===1;details.push({label:'Answer',student:a?.value||'Not answered',correct:q.answers[0],ok:correct});}
  else if(q.type==='double'||q.type==='triple'){const vals=a?.values||{};q.blanks.forEach((b,bi)=>{const ok=vals[bi]===b.answer;if(ok)marks++;details.push({label:b.label,student:vals[bi]||'Not answered',correct:b.answer,ok});});correct=marks===q.blanks.length;}
  else if(q.type==='se'){const vals=a?.values||[];const exact=vals.length===2&&q.answers.every(x=>vals.includes(x));marks=vals.filter(v=>q.answers.includes(v)).length;correct=exact;details.push({label:'Selected answers',student:vals.length?vals.join(', '):'Not answered',correct:q.answers.join(', '),ok:exact});}
  return {marks,correct,details};
}
